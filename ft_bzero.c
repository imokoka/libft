#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*temp_s;

	i = 0;
	temp_s = (char *)s;
	while (i < n)
	{
		temp_s[i] = 0;
		i++;
	}
}
